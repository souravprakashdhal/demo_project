package POJO;

public class Employee 
{
	int ID;
	String name,dept;
	public int getID() 
	{
		return ID;
	}
	public String getName() 
	{
		return name;
	}
	public String getDept() 
	{
		return dept;
	}
	public void setID(int id) 
	{
		this.ID = id;
	}
	public void setName(String name) 
	{
		this.name = name;
	}
	public void setDept(String dept) {
		this.dept = dept;
	}
	
}
